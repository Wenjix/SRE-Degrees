// Generates lib/cidg/catalog.ts from the merged CIDG incident specs (PR #3).
// Re-run after the specs change:  node scripts/gen-cidg-catalog.mjs
//
// Source of truth (committed):
//   rl-env/opensre-traj/specs/*.json        — synthetic CIDG scenarios
//   rl-env/opensre-traj/specs/real/*.json   — reconstructed real outages
// The dashboard is client-side, so we freeze a compact, render-ready subset into
// a TS module rather than read the JSON at runtime.

import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const SYN_DIR = join(ROOT, "rl-env/opensre-traj/specs");
const REAL_DIR = join(ROOT, "rl-env/opensre-traj/specs/real");
const OUT = join(ROOT, "lib/cidg/catalog.ts");

const cap = (s, n) => {
	if (s == null) return "";
	const str = String(s).replace(/\s+/g, " ").trim();
	return str.length > n ? `${str.slice(0, n - 1).trimEnd()}…` : str;
};
const titleize = (id) =>
	id.replace(/[_-]+/g, " ").replace(/\b\w/g, (c) => c.toUpperCase()).trim();

const TIERS = new Set(["autonomous", "approval", "blocked"]);
const normTier = (t) => (TIERS.has(t) ? t : "approval");

function load(dir, kind) {
	return readdirSync(dir)
		.filter((f) => f.endsWith(".json"))
		.map((f) => {
			const d = JSON.parse(readFileSync(join(dir, f), "utf8"));
			const ann = d.alert?.commonAnnotations ?? {};
			const answer = d.answer ?? {};
			const rem = d.remediation ?? {};
			const adversarial = Array.isArray(d.scenario?.adversarial_signals)
				? d.scenario.adversarial_signals
				: [];
			const trapActions = Array.isArray(rem.trap_actions) ? rem.trap_actions : [];
			return {
				id: d.incident ?? f.replace(/\.json$/, ""),
				file: `${kind === "real" ? "real/" : ""}${f}`,
				title: titleize(d.incident ?? f.replace(/\.json$/, "")),
				kind,
				provider: d.provider ?? "—",
				sourceCompany: d.source_company ?? null,
				sourceUrl: d.source_url ?? null,
				failureMode: d.failure_mode ?? "—",
				severity: d.default_severity ?? "—",
				difficulty: Number(d.scenario_difficulty ?? 0),
				rootCauseCategory: answer.root_cause_category ?? "unknown",
				rootCauseSubtype: cap(answer.root_cause_subtype, 320),
				loudSymptom: cap(ann.summary ?? d.alert?.title, 360),
				suspectedSymptom: cap(ann.suspected_symptom, 160),
				trap: cap(trapActions[0] ?? adversarial[0], 380),
				correctFix: cap(rem.canonical_fix, 460),
				fixTool: rem.fix_tool ?? "—",
				trustTier: normTier(rem.trust_tier),
				adversarialSignals: adversarial.slice(0, 3).map((s) => cap(s, 300)),
				redHerrings: (Array.isArray(answer.ruling_out_keywords) ? answer.ruling_out_keywords : [])
					.slice(0, 8)
					.map((s) => cap(s, 120)),
				requiredKeywords: (Array.isArray(answer.required_keywords) ? answer.required_keywords : [])
					.slice(0, 12)
					.map((s) => cap(s, 80)),
			};
		});
}

const incidents = [...load(REAL_DIR, "real"), ...load(SYN_DIR, "synthetic")].sort(
	(a, b) => b.difficulty - a.difficulty || a.id.localeCompare(b.id),
);

const header = `// GENERATED by scripts/gen-cidg-catalog.mjs — do not edit by hand.
// Source: rl-env/opensre-traj/specs/**.json (merged in PR #3). Re-run the script
// to regenerate. ${incidents.length} incidents (${incidents.filter((i) => i.kind === "real").length} real + ${incidents.filter((i) => i.kind === "synthetic").length} synthetic).
import type { TrustTier } from "./shared";

export type CidgIncident = {
	id: string;
	file: string;
	title: string;
	kind: "real" | "synthetic";
	provider: string;
	sourceCompany: string | null;
	sourceUrl: string | null;
	failureMode: string;
	severity: string;
	difficulty: number;
	rootCauseCategory: string;
	rootCauseSubtype: string;
	loudSymptom: string;
	suspectedSymptom: string;
	trap: string;
	correctFix: string;
	fixTool: string;
	trustTier: TrustTier;
	adversarialSignals: string[];
	redHerrings: string[];
	requiredKeywords: string[];
};

export const CIDG_INCIDENTS: CidgIncident[] = `;

writeFileSync(OUT, `${header}${JSON.stringify(incidents, null, "\t")};\n`);
console.log(`wrote ${incidents.length} incidents -> ${OUT}`);
